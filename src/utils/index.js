/**
 * 对比两个对象的值是否完全相等 返回值 true/false
 * @param {Object} a
 * @param {Object} b
 * @param {Boolean} true
 */
export function isObjectValueEqual(a, b, jumpObserver = true) {
  // 取对象a和b的属性名
  const aProps = Object.getOwnPropertyNames(a)
  const bProps = Object.getOwnPropertyNames(b)
  // 判断属性名的length是否一致
  if (aProps.length !== bProps.length) {
    return false
  }
  // 循环取出属性名，再判断属性值是否一致
  // vue中会给object添加__ob__需要跳过
  // _ob__: Observer这些数据是vue这个框架对数据设置的监控器，一般都是不可枚举的
  for (let i = 0; i < aProps.length; i++) {
    const propName = aProps[i]
    if (propName === '__ob__' && jumpObserver) {
      continue
    }
    if (a[propName] !== b[propName]) {
      return false
    }
  }
  return true
}
